void disp_hardware_init(void) {
    static uint8_t inited = false;
    if (inited)
        return;
    inited = true;
    Pad_Config(P1_1, PAD_PINMUX_MODE, PAD_IS_PWRON, PAD_PULL_UP, PAD_OUT_ENABLE, PAD_OUT_LOW);
    Pad_Config(P1_2, PAD_PINMUX_MODE, PAD_IS_PWRON, PAD_PULL_UP, PAD_OUT_ENABLE, PAD_OUT_HIGH);
    Pinmux_Config(P1_1, DWGPIO);
    Pinmux_Config(P1_2, DWGPIO);
    gpio_pin_configure_dt(&powr_ctrl, GPIO_OUTPUT);
    gpio_pin_configure_dt(&reset, GPIO_OUTPUT);
}
int disp_init(void) {
    disp_hardware_init();
    LOG_WRN("lcd ctrl gpio pin:%d,reset:%d", powr_ctrl.pin, reset.pin);
    // reset
    gpio_pin_set_dt(&reset, 1);
    k_msleep(10);
    gpio_pin_set_dt(&reset, 0);
    LOG_WRN("lcd start init...");
    i2c_dlps = false;
    ssd1306_128x32_i2c_init();
    ssd1306_clearScreen();

    if (bat_is_shutdown()) {
        i2c_dlps = true;
        return 0;
    }
    // vpp power on
    gpio_pin_set_dt(&powr_ctrl, 1);
    ssd1306_displayOn();
    k_msleep(100);

    uint32_t reset_type = AON_REG_READ(AON_NS_REG0X_APP) & 0xff;
    LOG_WRN("display inited!,reset:%d", reset_type);
    if (reset_type == 0 || aon_get_state(AON_STATE_RECOVER)) {
        aon_clear_state(AON_STATE_RECOVER);
        disp_welcom();
        k_work_reschedule(&disp_timeout, K_MSEC(3000));
    } else {
        //    disp_kb_status();
        k_work_reschedule(&disp_timeout, K_MSEC(500));
    }
    i2c_dlps = true;

    return 0;
}

void disp_sleep(void) {
    disp_hardware_init();
    k_work_cancel_delayable(&charge_work);
    k_work_cancel_delayable(&batlow_work);
    i2c_dlps = false;
    ssd1306_displayOff();
    i2c_dlps = true;
    cur_disp = DISP_NONE;
    // vpp power off
    gpio_pin_set_dt(&powr_ctrl, 0);
}
void disp_exit_sleep(void) {
    // vpp power on
    gpio_pin_set_dt(&powr_ctrl, 1);
    k_msleep(10);
    ssd1306_displayOn();
    k_work_reschedule(&disp_timeout, K_MSEC(100));
}
