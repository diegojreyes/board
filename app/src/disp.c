void disp_set_led_state(uint8_t state) {
    _kb_status stat;
    stat.caps_lock = (state & 0x02) ? 1 : 0;
    stat.num_lock = state & 0x01;
    // LOG_ERR("KB LED
    // STATE[%d,%d],cur[%d,%d]",kb_status.num_lock,kb_status.caps_lock,stat.num_lock,stat.caps_lock);
    if (kb_status.num_lock != stat.num_lock || kb_status.caps_lock != stat.caps_lock) {
        kb_status.num_lock = stat.num_lock;
        kb_status.caps_lock = stat.caps_lock;
        update_kb_status();
    }
}
void disp_set_winlock(uint8_t onoff) {
    kb_status.win_lock = onoff;
    update_kb_status();
}
void disp_set_mode(uint8_t mode) {
    kb_status.mode = mode;
    update_kb_status();
}
