static int endpoint_listener(const zmk_event_t *eh) {
    struct zmk_endpoint_changed *p = as_zmk_endpoint_changed(eh);
    if (p) {
        uint8_t index = 0;
        if (p->endpoint.transport == ZMK_TRANSPORT_USB) {
            index = 0;
        } else if (p->endpoint.transport == ZMK_TRANSPORT_PPT) {
            index = 4;
        } else if (p->endpoint.transport == ZMK_TRANSPORT_BLE) {
            index = p->endpoint.ble.profile_index + 1;
        }
        LOG_ERR("set mode :%d ", index);
        disp_set_mode(index);
    }
    return 0;
}

ZMK_LISTENER(disp_endpoint_listener, endpoint_listener);
ZMK_SUBSCRIPTION(disp_endpoint_listener, zmk_endpoint_changed);
