void disp_update_bat(uint8_t level) {
    static uint8_t last_index = 0;
    uint8_t index = (level + 10) / 20;
    if (charge_state.running)
        return;
    if (level > 0 && level < 20)
        index = 1;
    if (cur_disp == DISP_KB_STATUS || cur_disp == DISP_LIGHT_MODE) {
        if (last_index != index) {
            draw_img(20 - 12, 0, 12, 32, bat_imgs[index]);
            last_index = index;
        }
    }
}

void charge_work_cb(struct k_work *work) {
    static uint8_t last_index = 0;
    static uint8_t step = 0;
    if (charge_state.start) {
        charge_state.start = 0;
        last_index = 0;
        step = 0;
        LOG_DBG("charge work start");
    }
    uint8_t cur = (get_battery_level() + 10) / 20;
    if (cur == 0 && get_battery_level() > 0)
        cur = 1;
    if (cur == 5) {
        LOG_DBG("charge exit");
        return;
    } else {
        if (step == 6) {
            step = last_index;
            // LOG_DBG("reset setp:%d",step);
        }

        if (last_index != cur) {
            last_index = cur;
            if (step <= last_index) {
                step = last_index;
                LOG_DBG("update step:%d", step);
            }
        } else if (cur == 0) {
            if (step == 0) {
                step = 1;
            }
        }
    }
    // LOG_DBG("cur:%d,last:%d,step:%d",cur,last_index,step);
    if (cur_disp == DISP_KB_STATUS || cur_disp == DISP_LIGHT_MODE) {
        draw_img(20 - 12, 0, 12, 32, bat_imgs[step++]);
    }

    k_work_reschedule(&charge_work, K_MSEC(500));
}

void disp_bat_charging(void) {
    charge_state.start = 1;
    charge_state.running = 1;
    k_work_reschedule(&charge_work, K_MSEC(100));
    k_work_cancel_delayable(&batlow_work);
    // LOG_DBG("bat charging disp");
}
void disp_bat_charging_stop(void) {
    charge_state.running = 0;
    k_work_cancel_delayable(&charge_work);
    disp_update_bat(get_battery_level());
}

void batlow_work_cb(struct k_work *work) {
    static uint8_t flash = 0;
    if (cur_disp == DISP_KB_STATUS || cur_disp == DISP_LIGHT_MODE) {
        if (flash) {
            uint8_t index = (get_battery_level() + 10) / 20;
            if (index == 0 && get_battery_level() > 0)
                index = 1;
            draw_img(20 - 12, 0, 12, 32, bat_imgs[index]);
            LOG_DBG("dram img:%d", index);
        } else {
            ssd1306_clearBlock(8, 0, 12, 32);
        }
        flash = ~flash;
    }
    // LOG_DBG("bat low flash:%d",flash);
    k_work_reschedule(&batlow_work, K_MSEC(1000));
}
void disp_low_bat(bool en) {
    if (en && !charge_state.running) {
        if (k_work_delayable_remaining_get(&batlow_work) == 0) {
            k_work_reschedule(&batlow_work, K_MSEC(100));
            LOG_DBG("bat low restart");
        }

    } else {
        k_work_cancel_delayable(&batlow_work);
        disp_update_bat(get_battery_level());
    }
}

